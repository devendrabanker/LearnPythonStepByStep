#Goal: This program is to perform basic arithmetic operations like add, sub, multiplication, division

#Input: number1, number 2
#Output: result of the operations

#Input example 1: number 1 = 125, number2= 287357
#Output Expectation: 287482

number1 = int(input("Enter number1: "))
number2 = int(input("Enter number2: "))

#Addition
number3 = number1 + number2 #55
print(f"Additions of {number1} + {number2} = {number3}") #When we want to print variable


#Substraction
number4 = number1 - number2 #25
print(f"Additions of {number1} - {number2} = {number4}")

#Multiplicatino
number5 = number1 * number2
print(f"Additions of {number1} * {number2} = {number5}")

#Division
number6 = number1 / number2
print(f"Additions of {number1} / {number2} = {number6}")
